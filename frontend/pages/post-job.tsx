const Step5 = ({
    jobValues,
    setJobValues,
    setStep,
}: {
    jobValues: JobPostFormProps;
    setJobValues: React.Dispatch<React.SetStateAction<JobPostFormProps>>;
    setStep: React.Dispatch<React.SetStateAction<number>>;
}) => {
    const handleSubmit = async (event: any) => {
        event.preventDefault();
        const checkoutSessionResponse = await fetch("/api/checkout_session", {
            method: "POST",
            body: JSON.stringify(jobValues || {}),
        });

        const data: CheckoutSessionResponse =
            await checkoutSessionResponse.json();

        window.location.href = data.url;
    };
    return (
        <form onSubmit={handleSubmit}>
            <FormControl isRequired>
                <FormLabel>Plan Duration</FormLabel>
                <Select
                    size={"lg"}
                    id="planDuration"
                    background={"white"}
                    color="black"
                    width={{ base: "100%" }}
                    onChange={(e) =>
                        setJobValues({
                            ...jobValues,
                            planDuration: +e.target.value,
                        })
                    }
                    value={jobValues.planDuration}
                >
                    {[1, 2, 3, 4, 5, 6].map((x) => {
                        return (
                            <option key={x * 30} value={x * 30}>
                                {x * 30} Days
                            </option>
                        );
                    })}
                </Select>
            </FormControl>
            <Divider my="1rem" />
            <PlanTypes
                duration={jobValues.planDuration}
                jobValues={jobValues}
                setJobValues={setJobValues}
            />

            <Divider my="1rem" />
            <Stack direction={["column", "row"]} width={"100%"} my="1rem">
                <Button onClick={() => setStep(4)}>Back</Button>
                <Button
                    type="submit"
                    color="white"
                    backgroundColor={"upfront.300"}
                >
                    Continue To Payment
                </Button>
            </Stack>
        </form>
    );
};

const PlanTypes: FC<{
    jobValues: JobPostFormProps;
    setJobValues: React.Dispatch<React.SetStateAction<JobPostFormProps>>;
    duration: number;
}> = ({ jobValues, setJobValues, duration }) => {
    const { getRadioProps } = useRadioGroup({
        name: "planType",
        onChange: (next) =>
            setJobValues({ ...jobValues, planType: next as PlanType }),
        value: jobValues.planType,
    });

    return (
        <FormControl isRequired>
            <FormLabel>Plan Type</FormLabel>
            <Stack direction={["column", "row"]} width={"100%"}>
                {["Standard" as PlanType, "Premium" as PlanType].map(
                    (value: PlanType) => {
                        const radio = getRadioProps({ value });
                        return (
                            <RadioCard
                                key={value}
                                duration={duration}
                                planType={value}
                                {...radio}
                            >
                                {value}
                            </RadioCard>
                        );
                    }
                )}
            </Stack>
        </FormControl>
    );
};

const RadioCard: FC<any> = (props) => {
    const { getInputProps, getCheckboxProps } = useRadio(props);

    const input = getInputProps();
    const checkbox = getCheckboxProps();

    return (
        <Box as="label" width={{ base: "100%", md: "50%" }}>
            <input {...input} />
            <Box
                {...checkbox}
                textAlign={"center"}
                padding="1rem"
                border={"1px solid"}
                borderRadius="5px"
                borderColor={"upfront.300"}
                background={"white"}
                color="black"
                fontSize={"1.5rem"}
                boxShadow={"lg"}
                cursor="pointer"
                borderWidth="1px"
                _checked={{
                    bg: "upfront.300",
                    color: "white",
                    borderColor: "upfront.300",
                }}
                _focus={{
                    boxShadow: "outline",
                }}
                px={5}
                py={3}
            >
                <Text fontSize={"2rem"}>The {props.children} Plan</Text>
                <Text fontWeight={800} fontSize={"2rem"}>
                    Â£
                    {(props.duration *
                        priceFactors[props.planType as PlanType]) /
                        30}
                </Text>
                {props.children}
            </Box>
        </Box>
    );
};

export interface JobPostFormProps {
    companyLogoURL: string;
    companyName: string;
    companyWebsite: string;
    currency: Currency;
    description: string;
    howToApply: string;
    location: string;
    maxSalary: number;
    minSalary: number;
    minYOE: number;
    title: string;
    visaSponsorship: boolean;
    loginEmail: string;
    planDuration: number;
    planType: PlanType;
}
